// an array
var names = ['Jerry', 'Elaine', 'George', 'Kramer'];
var alsoNames = new Array('Dennis', 'Frank', 'Dee', 'Mac');
console.log(names);
console.log(alsoNames);
// a round number
var roundNumber = Math.round('3.14');
console.log(roundNumber);
// today's date
var today = new Date();
console.log(today);
// a boolean object
var booleanObject = new Boolean(true);
console.log(booleanObject);
// infinity
var unquantifiablyBigNumber = Infinity;
console.log(unquantifiablyBigNumber);
// a string object
var hello = new String('Hello!');
var movie = 'Pulp Fiction';
var movieObj = new String('Pulp Fiction');
console.log(hello);
console.log(movie);
console.log(movieObj);
